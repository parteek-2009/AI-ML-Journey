#we will perform basic aggregation functions here...
import numpy as np
general_array = np.array([1,2,3,4,5])
sum_array = np.sum(general_array)
mean_array = np.mean(general_array)
minimum_array = np.min(general_array)
maximum_array = np.max(general_array)
standard_daviation_array = np.std(general_array)
varience_array = np.var(general_array)
print(sum_array,mean_array,minimum_array,maximum_array,standard_daviation_array,varience_array)